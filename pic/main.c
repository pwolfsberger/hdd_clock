#include "config.h"

#include "i2c_ctrl.h"
#include "lcd_display.h"

// xc8 Library 
#include <p18f2520.h>
#include <delays.h>
#include <stdint.h>

/*--------------------------------------------------------------------------------*/
/* DEFINES                                                                        */
/*--------------------------------------------------------------------------------*/

/*--------------------------------------------------------------------------------*/
/* FUNCTIONS                                                                      */
/*--------------------------------------------------------------------------------*/
void main (void)
{

	TRISC = 0b11011111; 	// PORTC bit 5 to output (0)

    i2c_open();
    lcd_open();

    lcd_write("HI DIMI", 7); // Test LCD

	while (1)
	{
		LATCbits.LATC5 = ~LATCbits.LATC5; // toggle LATC

		Delay1KTCYx(50);	// Delay 50 x 1000 = 50,000 cycles; 200ms @ 1MHz
        
	}

}
